    private void transportTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_transportTableMouseClicked
        try{

        int row =
            transportTable.getSelectedRow();

        if(row == -1){
            return;
        }

        String guestName =
            transportTable.getValueAt(row, 0).toString();

        String transportType =
            transportTable.getValueAt(row, 1).toString();

        txtCost.setText(
            transportTable.getValueAt(row, 2).toString()
        );

        // ===== transport combo =====

        for(int i=0; i<cmbTransport.getItemCount(); i++){

            String item =
                cmbTransport.getItemAt(i);

            if(item.contains(transportType)){

                cmbTransport.setSelectedIndex(i);

                selectedTransportId =

                    Integer.parseInt(
                        item.split(" - ")[0]
                    );

                break;
            }
        }

        // ===== guest combo =====

        for(int i=0; i<cmbGuest.getItemCount(); i++){

            String item =
                cmbGuest.getItemAt(i);

            if(item.contains(guestName)){

                cmbGuest.setSelectedIndex(i);

                selectedGuestId =

                    Integer.parseInt(
                        item.split(" - ")[0]
                    );

                break;
            }
        }

    } catch(Exception e){

        javax.swing.JOptionPane.showMessageDialog(
            this,
            e.getMessage()
        );
    }
    }//GEN-LAST:event_transportTableMouseClicked


    private void loadTransportTable(){

    try{

        java.sql.Connection con =
            DBConnection.getConnection();

        String sql =

        "SELECT guest.Name AS GuestName, " +
        "transport.Type, " +
        "transport.Cost " +

        "FROM guest_transport " +

        "JOIN guest ON " +
        "guest_transport.GuestID = guest.GuestID " +

        "JOIN transport ON " +
        "guest_transport.TransportID = transport.TransportID";

        java.sql.PreparedStatement ps =
            con.prepareStatement(sql);

        java.sql.ResultSet rs =
            ps.executeQuery();

        javax.swing.table.DefaultTableModel model =

        (javax.swing.table.DefaultTableModel)
        transportTable.getModel();

        model.setRowCount(0);

        while(rs.next()){

            model.addRow(new Object[]{

                rs.getString("GuestName"),
                rs.getString("Type"),
                rs.getDouble("Cost")
            });
        }

        con.close();

    } catch(Exception e){

        javax.swing.JOptionPane.showMessageDialog(
            this,
            e.getMessage()
        );
    }
}
    
    

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
         try {
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Select a reservation first!");
            return;
        }

        int reservationId = (int) jTable1.getValueAt(selectedRow, 0);

        java.sql.Connection con = DBConnection.getConnection();

        // 🟢 1. نجيب كل الخدمات
        java.sql.Statement st = con.createStatement();
        java.sql.ResultSet rs = st.executeQuery("SELECT Name FROM service");

        javax.swing.DefaultListModel<String> model = new javax.swing.DefaultListModel<>();

        while (rs.next()) {
            model.addElement(rs.getString("Name"));
        }

        // 🟢 2. نعرضها للمستخدم
        javax.swing.JList<String> list = new javax.swing.JList<>(model);
        list.setSelectionMode(javax.swing.ListSelectionModel.MULTIPLE_INTERVAL_SELECTION);
        javax.swing.JScrollPane scroll = new javax.swing.JScrollPane(list);

        int option = javax.swing.JOptionPane.showConfirmDialog(
                this,
                scroll,
                "Select Services",
                javax.swing.JOptionPane.OK_CANCEL_OPTION
        );

        if (option != javax.swing.JOptionPane.OK_OPTION) return;

        java.util.List<String> selectedServices = list.getSelectedValuesList();

        // 🔴 3. نحذف القديم
        String deleteSQL = "DELETE FROM reservation_service WHERE ReservationID=?";
        java.sql.PreparedStatement psDelete = con.prepareStatement(deleteSQL);
        psDelete.setInt(1, reservationId);
        psDelete.executeUpdate();

        // 🟢 4. نضيف الجديد
        for (String serviceName : selectedServices) {

            String getId = "SELECT ServiceID FROM service WHERE Name=?";
            java.sql.PreparedStatement ps = con.prepareStatement(getId);
            ps.setString(1, serviceName);
            java.sql.ResultSet rs2 = ps.executeQuery();

            if (rs2.next()) {
                int serviceId = rs2.getInt("ServiceID");

                String insert = "INSERT INTO reservation_service VALUES (?, ?)";
                java.sql.PreparedStatement ps2 = con.prepareStatement(insert);
                ps2.setInt(1, reservationId);
                ps2.setInt(2, serviceId);
                ps2.executeUpdate();
            }
        }

        javax.swing.JOptionPane.showMessageDialog(this, "Services Updated!");

        con.close();

        // 🔄 تحديث الجدول
        loadReservations();

    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton3ActionPerformed


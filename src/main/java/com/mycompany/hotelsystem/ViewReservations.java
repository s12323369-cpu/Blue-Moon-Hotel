    private void btnCancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelActionPerformed
        try {
    int selectedRow = jTable1.getSelectedRow();

    if (selectedRow == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please select a reservation!");
        return;
    }
    
    
    int confirm = javax.swing.JOptionPane.showConfirmDialog(
    this,
    "Are you sure you want to delete this reservation?",
    "Confirm",
    javax.swing.JOptionPane.YES_NO_OPTION
);

if (confirm != javax.swing.JOptionPane.YES_OPTION) return;

    int reservationId = (int) jTable1.getValueAt(selectedRow, 0);

    java.sql.Connection con = DBConnection.getConnection();
    
    String getRoomSQL = "SELECT RoomID FROM reservation WHERE ReservationID = ?";
java.sql.PreparedStatement psGet = con.prepareStatement(getRoomSQL);
psGet.setInt(1, reservationId);
java.sql.ResultSet rs = psGet.executeQuery();

int roomId = 0;
if (rs.next()) {
    roomId = rs.getInt("RoomID");
}

    String sql = "DELETE FROM reservation WHERE ReservationID = ?";
    java.sql.PreparedStatement ps = con.prepareStatement(sql);

    ps.setInt(1, reservationId);
    ps.executeUpdate();
    
    // 🔹 رجّعي الغرفة Available
String updateRoom = "UPDATE room SET Status='Available' WHERE RoomID=?";
java.sql.PreparedStatement psUpdate = con.prepareStatement(updateRoom);

psUpdate.setInt(1, roomId);
psUpdate.executeUpdate();

    javax.swing.JOptionPane.showMessageDialog(this, "Deleted Successfully!");

    ps.close();
    con.close();

    loadReservations();

} catch (Exception e) {
    javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
}
    }//GEN-LAST:event_btnCancelActionPerformed


  private void loadReservations() {
    try {
        javax.swing.table.DefaultTableModel model =
                (javax.swing.table.DefaultTableModel) jTable1.getModel();

        model.setRowCount(0); // تنظيف الجدول

        java.sql.Connection con = DBConnection.getConnection();
        java.sql.Statement st = con.createStatement();
        java.sql.ResultSet rs = st.executeQuery(
   "SELECT r.ReservationID, g.Name, r.RoomID, r.CheckIn, r.CheckOut, " +
"GROUP_CONCAT(s.Name SEPARATOR ', ') AS Services " +
"FROM reservation r " +
"JOIN guest g ON r.GuestID = g.GuestID " +
"LEFT JOIN reservation_service rs ON r.ReservationID = rs.ReservationID " +
"LEFT JOIN service s ON rs.ServiceID = s.ServiceID " +
"GROUP BY r.ReservationID"
);

        while (rs.next()) {
           model.addRow(new Object[]{
    rs.getInt("ReservationID"),
    rs.getString("Name"),
    rs.getInt("RoomID"),
    rs.getDate("CheckIn"),
    rs.getDate("CheckOut"),
    (rs.getString("Services") == null ? "No Services" : rs.getString("Services"))
});
        }

        rs.close();
        st.close();
        con.close();

    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
}

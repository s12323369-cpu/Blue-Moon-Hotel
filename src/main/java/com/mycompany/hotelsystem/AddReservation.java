private void calculateTotal() {
    try {
        RoomItem selectedRoom = (RoomItem) cmbRoom.getSelectedItem();
        if (selectedRoom == null) return;

        int roomId = selectedRoom.id;

        String checkIn = txtCheckIn.getText();
        String checkOut = txtCheckOut.getText();

        if (checkIn.isEmpty() || checkOut.isEmpty()) return;

        java.time.LocalDate inDate = java.time.LocalDate.parse(checkIn);
        java.time.LocalDate outDate = java.time.LocalDate.parse(checkOut);

        if (!outDate.isAfter(inDate)) return;

        Connection con = DBConnection.getConnection();

        double pricePerDay = 0;
        Statement st = con.createStatement();
        ResultSet rs = st.executeQuery("SELECT Price FROM room WHERE RoomID=" + roomId);

        if (rs.next()) {
            pricePerDay = rs.getDouble("Price");
        }

        long days = java.time.temporal.ChronoUnit.DAYS.between(inDate, outDate);
        double totalAmount = days * pricePerDay;

        txtAmount.setText(String.valueOf(totalAmount));

        con.close();

    } catch (Exception e) {
    
    }
}
   
private void loadServices() {
    try {
        Connection con = DBConnection.getConnection();
        Statement st = con.createStatement();
        ResultSet rs = st.executeQuery("SELECT Name FROM service");

        javax.swing.DefaultListModel<String> model = new javax.swing.DefaultListModel<>();

        while (rs.next()) {
            model.addElement(rs.getString("Name"));
        }

        jList1.setModel(model);

        con.close();
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, e.getMessage());
    }
}

private void loadAvailableRoomsByDate(String checkIn, String checkOut) {
    try {
        java.sql.Connection con = DBConnection.getConnection();

        String sql = "SELECT RoomID, Type FROM room r WHERE Status='Available' AND r.RoomID NOT IN (" +
                     "SELECT RoomID FROM reservation WHERE NOT (CheckOut <= ? OR CheckIn >= ?))";

        java.sql.PreparedStatement ps = con.prepareStatement(sql);
        ps.setDate(1, java.sql.Date.valueOf(checkIn));
        ps.setDate(2, java.sql.Date.valueOf(checkOut));

        java.sql.ResultSet rs = ps.executeQuery();

        cmbRoom.removeAllItems();

        while (rs.next()) {
            cmbRoom.addItem(new RoomItem(
                rs.getInt("RoomID"),
                rs.getString("Type")
            ));
        }

        con.close();

    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, e.getMessage());
    }
}


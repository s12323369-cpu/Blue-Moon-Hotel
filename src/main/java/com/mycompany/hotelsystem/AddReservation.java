private void loadGuests() {
    try {
        java.sql.Connection con = DBConnection.getConnection();
        java.sql.Statement st = con.createStatement();
        java.sql.ResultSet rs = st.executeQuery("SELECT GuestID, Name FROM guest");
        cmbGuest.removeAllItems();
        while (rs.next()) {
    cmbGuest.addItem(new GuestItem(
        rs.getInt("GuestID"),
        rs.getString("Name")
    ));
}

        con.close();
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, e.getMessage());
    }
}


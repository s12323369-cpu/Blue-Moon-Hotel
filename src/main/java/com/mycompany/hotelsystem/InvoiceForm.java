    private void btnGenerateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerateActionPerformed
       try {

    int reservationId =
        Integer.parseInt(txtReservationID.getText());

    java.sql.Connection con =
        DBConnection.getConnection();

    javax.swing.table.DefaultTableModel model =
        (javax.swing.table.DefaultTableModel)
        invoiceTable.getModel();

    model.setRowCount(0);

    double total = 0;
    double paid = 0;

    // ================= ROOM =================

    String roomSql =
    "SELECT guest.Name AS GuestName, room.RoomID, room.Price " +
    "FROM reservation " +
    "JOIN guest ON reservation.GuestID = guest.GuestID " +
    "JOIN room ON reservation.RoomID = room.RoomID " +
    "WHERE reservation.ReservationID=?";

    java.sql.PreparedStatement roomPs =
        con.prepareStatement(roomSql);

    roomPs.setInt(1, reservationId);

    java.sql.ResultSet roomRs =
        roomPs.executeQuery();

    if(roomRs.next()){

    txtGuestName.setText(
        roomRs.getString("GuestName")
    );

    txtRoom.setText(
        String.valueOf(roomRs.getInt("RoomID"))
    );

    double roomPrice =
        roomRs.getDouble("Price");

    model.addRow(new Object[]{
        "Room",
        "Room #" + roomRs.getInt("RoomID"),
        roomPrice
    });

    total += roomPrice;

} else {

    javax.swing.JOptionPane.showMessageDialog(
        this,
        "Reservation ID not found!"
    );

    con.close();
    return;
}

    // ================= SERVICES =================

    String serviceSql =
    "SELECT service.Name, service.Price " +
    "FROM reservation_service " +
    "JOIN service ON reservation_service.ServiceID = service.ServiceID " +
    "WHERE reservation_service.ReservationID=?";

    java.sql.PreparedStatement servicePs =
        con.prepareStatement(serviceSql);

    servicePs.setInt(1, reservationId);

    java.sql.ResultSet serviceRs =
        servicePs.executeQuery();

    while(serviceRs.next()){

        String serviceName =
            serviceRs.getString("Name");

        double price =
            serviceRs.getDouble("Price");

        model.addRow(new Object[]{
            "Service",
            serviceName,
            price
        });

        total += price;
    }

    // ================= FOOD ORDERS =================

String foodSql =
"SELECT ItemName, Quantity, Price " +
"FROM food_orders " +
"WHERE ReservationID=?";

java.sql.PreparedStatement foodPs =
    con.prepareStatement(foodSql);

foodPs.setInt(1, reservationId);

java.sql.ResultSet foodRs =
    foodPs.executeQuery();

while(foodRs.next()){

    String item =
        foodRs.getString("ItemName");

    int qty =
        foodRs.getInt("Quantity");

    double price =
        foodRs.getDouble("Price");

    model.addRow(new Object[]{
        "Food",
        item + " x" + qty,
        price
    });

    total += price;
}

    // ================= PAYMENTS =================

    String paymentSql =
    "SELECT Amount, Method FROM payment " +
    "WHERE ReservationID=?";

    java.sql.PreparedStatement paymentPs =
        con.prepareStatement(paymentSql);

    paymentPs.setInt(1, reservationId);

    java.sql.ResultSet paymentRs =
        paymentPs.executeQuery();

    while(paymentRs.next()){

    double amount =
        paymentRs.getDouble("Amount");

    String method =
        paymentRs.getString("Method");

    model.addRow(new Object[]{
    "Payment",
    method,
    String.format("%.2f", amount)
});
    

    paid += amount;
}

  double remaining = Math.max(0, total - paid);
model.addRow(new Object[]{
    "", "----------------", ""
});

model.addRow(new Object[]{
    "Total Cost",
    "",
    total
});

model.addRow(new Object[]{
    "Paid",
    "",
    paid
});

model.addRow(new Object[]{
    "Remaining",
    "",
    remaining
});
txtTotal.setText(String.valueOf(remaining));

  String insertInvoice =
"INSERT INTO invoice " +
"(ReservationID, TotalAmount, PaidAmount, RemainingAmount) " +
"VALUES(?,?,?,?)";

java.sql.PreparedStatement invoicePs =
    con.prepareStatement(insertInvoice);

invoicePs.setInt(1, reservationId);
invoicePs.setDouble(2, total);
invoicePs.setDouble(3, paid);
invoicePs.setDouble(4, remaining);

invoicePs.executeUpdate();
    con.close();

} catch (Exception e) {

    javax.swing.JOptionPane.showMessageDialog(
        this,
        e.getMessage()
    );
}
    }//GEN-LAST:event_btnGenerateActionPerformed


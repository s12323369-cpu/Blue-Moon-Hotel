    private void txtReservationIDKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtReservationIDKeyReleased
       try {

    if(txtReservationID.getText().isEmpty()){

    txtGuestName.setText("");
    txtRoom.setText("");
    txtTotal.setText("");

    return;
}

    int reservationId =
        Integer.parseInt(txtReservationID.getText());

    java.sql.Connection con =
        DBConnection.getConnection();

    String sql =
    "SELECT guest.Name AS GuestName, room.RoomID " +
    "FROM reservation " +
    "JOIN guest ON reservation.GuestID = guest.GuestID " +
    "JOIN room ON reservation.RoomID = room.RoomID " +
    "WHERE reservation.ReservationID=?";

    java.sql.PreparedStatement ps =
        con.prepareStatement(sql);

    ps.setInt(1, reservationId);

    java.sql.ResultSet rs =
        ps.executeQuery();

    if(rs.next()){

        txtGuestName.setText(
            rs.getString("GuestName")
        );

        txtRoom.setText(
            String.valueOf(rs.getInt("RoomID"))
        );

    } else {

        txtGuestName.setText("");
        txtRoom.setText("");
    }

    con.close();

} catch(Exception e){

    txtGuestName.setText("");
    txtRoom.setText("");
     txtTotal.setText("");
}
    }//GEN-LAST:event_txtReservationIDKeyReleased


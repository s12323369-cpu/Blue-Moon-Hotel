    private void btnAddOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddOrderActionPerformed
      String item = itemList.getSelectedValue();

if(item == null){
    javax.swing.JOptionPane.showMessageDialog(this, "Select item!");
    return;
}

String priceText = lblPrice.getText()
        .replace("Price: $", "");

double price = Double.parseDouble(priceText);

boolean found = false;

for(int i = 0; i < model.getRowCount(); i++){

    String existingItem = model.getValueAt(i, 0).toString();

    if(existingItem.equals(item)){

        int qty = Integer.parseInt(model.getValueAt(i, 1).toString());

        qty++;

        model.setValueAt(qty, i, 1);

        model.setValueAt(qty * price, i, 2);

        found = true;

        break;
    }
}

if(!found){

    model.addRow(new Object[]{
        item,
        1,
        price
    });
}

total += price;

lblTotal.setText("Total: $" + total);
    }//GEN-LAST:event_btnAddOrderActionPerformed


    private void btnPlaceOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPlaceOrderActionPerformed
        try{

    java.sql.Connection con = DBConnection.getConnection();
    String selected =
            guestComboBox.getSelectedItem().toString();

        int reservationId =
            Integer.parseInt(
                selected.split(" - ")[0]
            );
    
  String sql =
"INSERT INTO food_orders" +
"(ReservationID, ItemName, Quantity, Price) " +
"VALUES(?,?,?,?)";
   
    java.sql.PreparedStatement ps = con.prepareStatement(sql);

    for(int i = 0; i < model.getRowCount(); i++){

    if(model.getValueAt(i,0) == null){
        continue;
    }

    ps.setInt(1, reservationId);

ps.setString(2,
    model.getValueAt(i,0).toString()
);

ps.setInt(3,
    Integer.parseInt(model.getValueAt(i,1).toString())
);

ps.setDouble(4,
    Double.parseDouble(model.getValueAt(i,2).toString())
);

    ps.executeUpdate();
}

    javax.swing.JOptionPane.showMessageDialog(this, "Order placed!");

    model.setRowCount(0);

    total = 0;

    lblTotal.setText("Total: $0");

    con.close();

}catch(Exception e){

    javax.swing.JOptionPane.showMessageDialog(this, e.getMessage());
}
    }//GEN-LAST:event_btnPlaceOrderActionPerformed


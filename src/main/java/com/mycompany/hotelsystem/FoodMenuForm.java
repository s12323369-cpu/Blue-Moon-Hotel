    private void categoryListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_categoryListValueChanged
        String selectedCategory = categoryList.getSelectedValue();

if (selectedCategory == null) return;

try {

    java.sql.Connection con = DBConnection.getConnection();

    String sql = "SELECT Name FROM foodmenu WHERE Category=?";

    java.sql.PreparedStatement ps = con.prepareStatement(sql);

    ps.setString(1, selectedCategory);

    java.sql.ResultSet rs = ps.executeQuery();

    javax.swing.DefaultListModel<String> foodModel =
            new javax.swing.DefaultListModel<>();

    while (rs.next()) {

        foodModel.addElement(rs.getString("Name"));
    }

    itemList.setModel(foodModel);

    con.close();

} catch (Exception e) {

    javax.swing.JOptionPane.showMessageDialog(this, e.getMessage());
}
    }//GEN-LAST:event_categoryListValueChanged


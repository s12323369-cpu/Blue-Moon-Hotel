    private void eventTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_eventTableMouseClicked


    try {

        int row = eventTable.getSelectedRow();

        if(row == -1){
            return;
        }

        // الأعمدة الصحيحة
        String guestName =
            eventTable.getValueAt(row, 0).toString();

        String eventName =
            eventTable.getValueAt(row, 1).toString();

        txtDate.setText(
            String.valueOf(eventTable.getValueAt(row, 2))
        );

        txtLocation.setText(
            String.valueOf(eventTable.getValueAt(row, 3))
        );

        txtPrice.setText(
            String.valueOf(eventTable.getValueAt(row, 4))
        );

        // ===== event combo =====

        for(int i=0; i<cmbEvent.getItemCount(); i++){

            String item = cmbEvent.getItemAt(i);

            if(item.contains(eventName)){

                cmbEvent.setSelectedIndex(i);

                selectedEventId =
                    Integer.parseInt(
                        item.split(" - ")[0]
                    );

                break;
            }
        }

        // ===== guest combo =====

        for(int i=0; i<cmbGuest.getItemCount(); i++){

            String item = cmbGuest.getItemAt(i);

            if(item.contains(guestName)){

                cmbGuest.setSelectedIndex(i);

                selectedGuestId =
                    Integer.parseInt(
                        item.split(" - ")[0]
                    );

                break;
            }
        }

    } catch(Exception e){

        javax.swing.JOptionPane.showMessageDialog(
            this,
            e.getMessage()
        );
    }

    }//GEN-LAST:event_eventTableMouseClicked


    private void cmbEventActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbEventActionPerformed
       try {

    if(cmbEvent.getSelectedItem() == null){
        return;
    }

    String selected =
        cmbEvent.getSelectedItem().toString();

    int eventId =
        Integer.parseInt(
            selected.split(" - ")[0]
        );

    java.sql.Connection con =
        DBConnection.getConnection();

    String sql =
        "SELECT * FROM event WHERE EventID=?";

    java.sql.PreparedStatement ps =
        con.prepareStatement(sql);

    ps.setInt(1, eventId);

    java.sql.ResultSet rs =
        ps.executeQuery();

    if(rs.next()){

        txtDate.setText(
            rs.getString("Date")
        );

        txtLocation.setText(
            rs.getString("Location")
        );

        txtCapacity.setText(
            String.valueOf(
                rs.getInt("Capacity")
            )
        );

        txtPrice.setText(
            String.valueOf(
                rs.getDouble("Price")
            )
        );
    }

    con.close();

} catch(Exception e){

    javax.swing.JOptionPane.showMessageDialog(
        this,
        e.getMessage()
    );
}
    }//GEN-LAST:event_cmbEventActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
      
    try {

        if(selectedEventId == -1){

            javax.swing.JOptionPane.showMessageDialog(
                this,
                "Select row first!"
            );

            return;
        }

        java.sql.Connection con =
            DBConnection.getConnection();

        String sql =
            "UPDATE event SET Date=?, Location=?, Capacity=?, Price=? WHERE EventID=?";

        java.sql.PreparedStatement ps =
            con.prepareStatement(sql);

        ps.setString(1, txtDate.getText());

        ps.setString(2, txtLocation.getText());

        ps.setInt(3,
            Integer.parseInt(txtCapacity.getText())
        );

        ps.setDouble(4,
            Double.parseDouble(txtPrice.getText())
        );

        ps.setInt(5, selectedEventId);

        ps.executeUpdate();

        javax.swing.JOptionPane.showMessageDialog(
            this,
            "Updated Successfully!"
        );

        loadEventTable();

        con.close();

    } catch(Exception e){

        javax.swing.JOptionPane.showMessageDialog(
            this,
            e.getMessage()
        );
    }

    }//GEN-LAST:event_btnUpdateActionPerformed


    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
      try {
    int row = jTable1.getSelectedRow();

    if (row == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Select a guest!");
        return;
    }

    int id = (int) jTable1.getValueAt(row, 0);

    java.sql.Connection con = DBConnection.getConnection();

    String sql = "DELETE FROM guest WHERE GuestID=?";
    java.sql.PreparedStatement ps = con.prepareStatement(sql);

    ps.setInt(1, id);
    ps.executeUpdate();

    javax.swing.JOptionPane.showMessageDialog(this, "Deleted!");

    con.close();
    clearFields();
loadGuests();

} catch (Exception e) {
    javax.swing.JOptionPane.showMessageDialog(this, e.getMessage());
}
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      try {

    String name = txtGuestName.getText();
    String phone = txtPhone.getText();

    if(name.isEmpty() || phone.isEmpty()){

    javax.swing.JOptionPane.showMessageDialog(
        this,
        "Please fill all fields!"
    );

    return;
}

if(!phone.matches("\\d+")){

    javax.swing.JOptionPane.showMessageDialog(
        this,
        "Phone must contain numbers only!"
    );

    return;
}
    java.sql.Connection con = DBConnection.getConnection();

    String sql = "INSERT INTO guest(Name, Phone) VALUES (?, ?)";
 
    
    java.sql.PreparedStatement ps = con.prepareStatement(sql);

    ps.setString(1, name);
    ps.setString(2, phone);

    ps.executeUpdate();

    JOptionPane.showMessageDialog(this, "Guest Added!");

    txtGuestName.setText("");
    txtPhone.setText("");

    clearFields();
    loadGuests();

    con.close();

} catch (Exception e) {

    JOptionPane.showMessageDialog(this, e.getMessage());
}
    }//GEN-LAST:event_jButton1ActionPerformed


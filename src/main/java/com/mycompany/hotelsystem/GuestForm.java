    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       try {

    if(selectedGuestId == -1) {
        JOptionPane.showMessageDialog(this, "Select a guest first!");
        return;
    }

    String name = txtGuestName.getText();
    String phone = txtPhone.getText();

    java.sql.Connection con = DBConnection.getConnection();

    String sql = "UPDATE guest SET Name=?, Phone=? WHERE GuestID=?";

    java.sql.PreparedStatement ps = con.prepareStatement(sql);

    ps.setString(1, name);
    ps.setString(2, phone);
    ps.setInt(3, selectedGuestId);

    ps.executeUpdate();

    JOptionPane.showMessageDialog(this, "Updated Successfully!");

    txtGuestName.setText("");
    txtPhone.setText("");

    selectedGuestId = -1;

    clearFields();
loadGuests();

    con.close();

} catch (Exception e) {

    JOptionPane.showMessageDialog(this, e.getMessage());
}
    }//GEN-LAST:event_jButton3ActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
      try {

    String search = txtSearch.getText();

    javax.swing.table.DefaultTableModel model =
        (javax.swing.table.DefaultTableModel) jTable1.getModel();

    model.setRowCount(0);
   if(search.isEmpty()) {
    loadGuests();
    return;
   }
   
    java.sql.Connection con = DBConnection.getConnection();

    String sql = "SELECT * FROM guest WHERE Name LIKE ?";

    java.sql.PreparedStatement ps = con.prepareStatement(sql);

    ps.setString(1, "%" + search + "%");

    java.sql.ResultSet rs = ps.executeQuery();

    while(rs.next()) {

        model.addRow(new Object[]{

            rs.getInt("GuestID"),
            rs.getString("Name"),
            rs.getString("Phone")
        });
    }

    con.close();

} catch (Exception e) {

    JOptionPane.showMessageDialog(this, e.getMessage());
}
    }//GEN-LAST:event_btnSearchActionPerformed

